import turtle
l=turtle.Turtle()
turtle.title("Window Logo")
turtle.bgcolor("black")
turtle.setup(1000,700)
l.speed(2)
def move(x,y):
    l.penup()
    l.goto(x,y)
    l.pendown()
l.pensize(3)
l.color("#0078D7")
l.begin_fill()
l.fillcolor("#0078D7")

l.left(90)
l.forward(150)
l.right(80)
l.forward(200)
y=l.ycor()
print(y)
l.right(100)
a=y-150
l.forward(y+a)
print(l.ycor())
l.home()
l.end_fill()

l.pensize(4)

move(0,75)
l.color("black")
l.forward(197)
move(93,75)
l.left(90)
l.forward(100)
l.backward(200)
l.hideturtle()
turtle.mainloop()